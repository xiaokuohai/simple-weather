package cn.udday.simpleweather.Beans;

public class BaseBean {
    private int code;
    private String msg;
    private String data;
    private int time;
    private long log_id;

    public int getCode() {
        return code;
    }

    public String getMsg() {
        return msg;
    }

    public String getData() {
        return data;
    }

    public int getTime() {
        return time;
    }

    public long getLog_id() {
        return log_id;
    }
}
